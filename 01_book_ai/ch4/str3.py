verb = 'singing'
print('-------------------------')
print(' verb = ', verb)
print('-------------------------')

print('verb[0]', verb[0])
print('verb[2]', verb[2])

print('verb[-1]', verb[-1])

# start
print('verb[:]', verb[:])
print('verb[4:]', verb[4:])
print('verb[-3:]', verb[-3:])

# end
print('verb[:0]', verb[:0])
print('verb[:4]', verb[:4])

# start:end
print('verb[1:2]', verb[1:2])
print('verb[2:5]', verb[2:5])

# start:end:step
str = '1,2,3,4,5,6,7,8,9'
print('str[::1]', str[::1])
print('str[::2]', str[::2])
print('str[1::2]', str[1::2])
print('str[0::2]', str[0::2])

# back 
print('str[::-1]', str[::-1])
